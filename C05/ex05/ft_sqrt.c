int	ft_sqrt(int nb)
{
	int	i;

	i = 2;
	if (nb <= 0 || nb > 2147483647)
		return (0);
	if (nb == 1)
		return (1);
	while (i < nb / i)
		i++;
	if (nb / i == i)
		return (i);
	else
		return (0);
}
