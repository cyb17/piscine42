#include <stdio.h>
int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2 || nb > 2147483647)
		return (0);
	while (nb % i != 0)
		i++;
	if (i < nb)
		return (0);
	else
		return (1);
}

int	main(void)
{
	printf("%d", ft_is_prime(2147483647));
	return (0);
}
